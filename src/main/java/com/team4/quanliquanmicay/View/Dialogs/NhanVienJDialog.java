    /**
     * Filter v√† fill d·ªØ li·ªáu theo Status v√† Role ƒë∆∞·ª£c ch·ªçn
     */
    private void filterAndFillTable() {
        // X√≥a d·ªØ li·ªáu c≈© trong b·∫£ng
        DefaultTableModel model = (DefaultTableModel) tableInfo.getModel();
        model.setRowCount(0);
        
        try {
            // L·∫•y ƒëi·ªÅu ki·ªán filter
            String selectedStatus = (String) cboStatus.getSelectedItem();
            String selectedRole = (String) cboRole.getSelectedItem();
            
            // L·∫•y danh s√°ch nh√¢n vi√™n t·ª´ database
            List<UserAccount> employees = userDAO.findAll();
            
            // Filter theo ƒëi·ªÅu ki·ªán
            List<UserAccount> filteredEmployees = filterEmployees(employees, selectedStatus, selectedRole);
            
            // ƒê·ªï d·ªØ li·ªáu ƒë√£ filter v√†o b·∫£ng
            for (UserAccount emp : filteredEmployees) {
                Object[] row = {
                    emp.getUser_id(),           // M√£ nh√¢n vi√™n
                    emp.getUsername(),         // T√†i kho·∫£n
                    emp.getPass(),         // M·∫≠t kh·∫©u
                    emp.getFullName(),         // H·ªç v√† t√™n
                    emp.getGender() != null ? (emp.getGender() == 1 ? "Nam" : "N·ªØ") : "Kh√¥ng x√°c ƒë·ªãnh",           // Gi·ªõi t√≠nh: 1=Nam, 0=N·ªØ
                    emp.getPhone_number(),            // SƒêT
                    emp.getEmail(),            // Email
                    emp.getIs_enabled() != null ? (emp.getIs_enabled() == 1 ? "Ho·∫°t ƒë·ªông" : "Kh√¥ng ho·∫°t ƒë·ªông") : "Kh√¥ng x√°c ƒë·ªãnh", // Tr·∫°ng th√°i
                    getRoleName(emp.getRole_id()),     // Vai tr√≤ - Hi·ªÉn th·ªã t√™n thay v√¨ ID
                    emp.getCreated_date()       // Ng√†y t·∫°o
                };
                model.addRow(row);
            }
            
            System.out.println("ƒê√£ filter v√† load " + filteredEmployees.size() + "/" + employees.size() + " nh√¢n vi√™n");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "L·ªói khi filter d·ªØ li·ªáu: " + e.getMessage());
            e.printStackTrace();
        }
    }

    /**
     * Filter danh s√°ch nh√¢n vi√™n theo Status v√† Role
     */
    private List<UserAccount> filterEmployees(List<UserAccount> employees, String selectedStatus, String selectedRole) {
        List<UserAccount> filtered = new ArrayList<>();
        
        for (UserAccount emp : employees) {
            boolean matchStatus = true;
            boolean matchRole = true;
            
            // Filter theo Status
            if (selectedStatus != null && !selectedStatus.equals("T·∫•t c·∫£")) {
                if (selectedStatus.equals("Ho·∫°t ƒë·ªông")) {
                    matchStatus = (emp.getIs_enabled() != null && emp.getIs_enabled() == 1);
                } else if (selectedStatus.equals("Kh√¥ng ho·∫°t ƒë·ªông")) {
                    matchStatus = (emp.getIs_enabled() != null && emp.getIs_enabled() == 0);
                }
            }
            
            // Filter theo Role - c·∫ßn extract role_id t·ª´ display text
            if (selectedRole != null && !selectedRole.equals("T·∫•t c·∫£")) {
                String roleId = extractRoleId(selectedRole);
                matchRole = roleId.equals(emp.getRole_id());
            }
            
            // Ch·ªâ th√™m v√†o danh s√°ch n·∫øu th·ªèa m√£n c·∫£ 2 ƒëi·ªÅu ki·ªán
            if (matchStatus && matchRole) {
                filtered.add(emp);
            }
        }
        
        return filtered;
    }
    
    /**
     * Extract role_id t·ª´ display text (VD: "R001 - Manager" -> "R001")
     */
    private String extractRoleId(String displayText) {
        if (displayText != null && displayText.contains(" - ")) {
            return displayText.split(" - ")[0];
        }
        return displayText; // Fallback
    }

    /**
     * L·∫•y t√™n vai tr√≤ t·ª´ cache (nhanh h∆°n)
     */
    private String getRoleName(String roleId) {
        if (roleId != null && roleMap.containsKey(roleId)) {
            return roleMap.get(roleId); // Manager, Staff
        }
        return "Kh√¥ng x√°c ƒë·ªãnh";
    }

